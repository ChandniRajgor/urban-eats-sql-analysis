#restaurants with high revenue(income percentage)
with restaurant_order_price as
(
select restaurant_name, sum(total_order) as total_order_price,max(income_percentage) as inc_pct
from
orders o 
join restaurants r
on o.restaurants_id=r.id
group by restaurant_name
order by total_order_price desc)

select restaurant_name,total_order_price,round((total_order_price*inc_pct),2) as Revenue from restaurant_order_price 
order by Revenue desc limit 10;


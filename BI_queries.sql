with avg_meal_price as
(
select 
       meal_type,total_order
from orders o 
join order_details od
on o.id=od.order_id
join meals me
on me.id=od.meals_id
join meal_types mt
on me.meal_types_id=mt.id
)
select meal_type, avg(total_order) as avg_price
from avg_meal_price
group by meal_type
order by avg_price desc;


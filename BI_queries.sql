    SELECT  
        restaurant_types , 
       restaurant_name,
	   count(o.id) as order_count
    FROM restaurant_types rt
    JOIN restaurants r
        ON rt.id = r.restaurant_types_id
	join orders o
    on r.id=o.restaurants_id
    group by restaurant_name,restaurant_types
)
SELECT *
FROM (
    SELECT 
        restaurant_types,
       restaurant_name,
        order_count,
         DENSE_RANK() OVER (
            PARTITION BY restaurant_types
            ORDER BY order_count DESC
        ) AS rnk
    FROM Top_rest_categorywise
) t
WHERE rnk <= 3;



# Q3 monthwise top 5 customers
WITH Monthwise_top_customer AS (
    SELECT  
        CONCAT(m.first_name, ' ', m.surname) AS customer_name, 
        mmt.order_count,
        mmt.month
    FROM members m
    JOIN monthly_member_totals mmt
        ON m.id = mmt.members_id
)

SELECT *
FROM (
    SELECT 
        customer_name,
        month,
        order_count,
         DENSE_RANK() OVER (
            PARTITION BY month 
            ORDER BY order_count DESC
        ) AS rnk
    FROM Monthwise_top_customer
) t
WHERE rnk <= 5;

#  meal type by gender
with mealstype_gender as
(select sex,count(o.id) as total_orders ,meal_type
from members m
join orders o 
on m.id=o.members_id
join order_details od
on o.id=od.order_id
join meals me
on me.id=od.meals_id
join meal_types mt
on mt.id=me.meal_types_id
group by sex,meal_type
)
select * 
from( select  
             sex,
			 meal_type,
             total_orders,
             dense_rank() over(
                               partition by sex
                               order by total_orders desc
                               ) as rnk
from mealstype_gender) t
where rnk<=5 ;


